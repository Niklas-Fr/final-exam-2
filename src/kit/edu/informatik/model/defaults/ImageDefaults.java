package kit.edu.informatik.model.defaults;

/**
 * This enum represents the different sizes of images.
 *
 * @author uqtwh
 * @version 1.0
 */
public enum ImageDefaults {

    /**
     * The size of an icon.
     */
    ICON,

    /**
     * The size of a small image.
     */
    SMALL,

    /**
     * The size of a medium image.
     */
    MEDIUM,

    /**
     * The size of a large image.
     */
    LARGE;
}
