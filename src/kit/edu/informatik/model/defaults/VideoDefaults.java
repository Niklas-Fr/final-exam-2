package kit.edu.informatik.model.defaults;

/**
 * Enum for the length of a video.
 *
 * @author uqtwh
 * @version 1.0
 */
public enum VideoDefaults {

    /**
     * The length of a video clip.
     */
    CLIP,

    /**
     * The length of a short video.
     */
    SHORT,

    /**
     * The length of a movie.
     */
    MOVIE,

    /**
     * The length of a long video.
     */
    LONG;
}
