package kit.edu.informatik.model.defaults;

/**
 * Enum for the length of an audio file.
 *
 * @author uqtwh
 * @version 1.0
 */
public enum AudioDefaults {

    /**
     * Sample length.
     */
    SAMPLE,

    /**
     * Short length.
     */
    SHORT,

    /**
     * Normal length.
     */
    NORMAL,

    /**
     * Long length.
     */
    LONG;
}
