package kit.edu.informatik.commands;

/**
 * Enum for the user commands.
 *
 * @author utqwh
 * @version 1.0
 */
public enum UserCommands {

    /**
     * The command to load a file.
     */
    LOAD,

    /**
     * The command to change the access amount of a document.
     */
    CHANGE,

    /**
     * The command to run a document, meaning sorting it efficiently.
     */
    RUN
}
